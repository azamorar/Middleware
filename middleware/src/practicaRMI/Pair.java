package practicaRMI;


public class Pair {
	private Integer left;
	private Integer right;

	public Pair(Integer left, Integer right) {
		this.left = left;
		this.right = right;
	}

	public Integer getLeft() {
		return left;
	}

	public Integer getRight() {
		return right;
	}
	
	public void setLeft(Integer num) {
		left = num;
		
	}
	
	public void setRight(Integer num) {
		right = num;
		
	}

	
}


